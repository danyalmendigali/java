package JavaLessons2;

public class Variables_double_float {
    public static void main(String[] args) {
        // double 64 bits
        // float 32 bits
        double a = 5.7;
        double b = 4.11;
        double sum = a + b;
        float aa = 9.3f;
        sum = a + aa;

        System.out.println(sum);
    }
}
